package org.example.AssuitSheet.NewComers.Sheet1;

import java.util.Scanner;

//https://codeforces.com/group/MWSDmqGsZm/contest/219158/problem/B
public class B {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int my_int = scan.nextInt();
        long my_long = scan.nextLong();
        char my_char = scan.next().charAt(0);
        float my_float = scan.nextFloat();
        double my_double = scan.nextDouble();
        System.out.print(my_int + "\n" + my_long + "\n" + my_char + "\n" + my_float + "\n"+my_double);
    }
}
