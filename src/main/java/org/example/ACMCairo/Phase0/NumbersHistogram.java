import java.util.*;

public class NumbersHistogram {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        String s = scan.next();
        char c = s.charAt(0);
        int t = scan.nextInt();
        while (t-- > 0) {
            int x = scan.nextInt();
            for (int i = 0; i < x; i++) {
                System.out.print(c);

            }
            System.out.println("");


        }

    }


}